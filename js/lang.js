/* =========================
   LANGUAGE STATE
========================= */
export function getLang(){
  return localStorage.getItem("lang") || "fr";
}

export function setLang(lang){
  localStorage.setItem("lang", lang);
}

export function translate(key){
  return translations[getLang()]?.[key] || key;
}

/* =========================
   APPLY LANGUAGE
========================= */
export function applyLang(){

  const lang = getLang();
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;

    const text = translations[lang]?.[key];

    if(!text) return;

    if(el.tagName === "INPUT"){
      el.placeholder = text;
    } else {
      el.textContent = text;
    }
  });

  const navMap = {
    "/": "home",
    "/index.html": "home",
    "/browse.html": "browse",
    "/genres.html": "genres",
    "/charts.html": "charts",
    "/new-releases.html": "new",
    "/user/library.html": "library",
    "/user/purchases.html": "purchases",
    "/user/settings.html": "settings",
    "/artist/dashboard.html": "dashboard",
    "/artist/catalog.html": "catalog",
    "/artist/upload-track.html": "uploadTrack",
    "/artist/upload-album.html": "uploadAlbum",
    "/artist/payouts.html": "payouts",
    "/artist/analytics.html": "analytics"
  };

  document.querySelectorAll(".side-menu a[href]").forEach(el => {
    const href = el.getAttribute("href");
    const key = navMap[href];

    if(!key) return;

    const text = translations[lang]?.[key];
    if(text) el.textContent = text;
  });

  /* update switch label */
  const btn = document.getElementById("langBtn");
  if(btn){
    btn.textContent = lang.toUpperCase() + " ▾";
  }
}

/* =========================
   TOGGLE LANGUAGE
========================= */
export function toggleLang(){
  const current = getLang();
  const next = current === "fr" ? "en" : "fr";

  setLang(next);
  applyLang();
  attachLangButton();
}

function ensureLangButton(){
  let btn = document.getElementById("langBtn");
  if(btn) return btn;

  if(!document.body) return null;

  btn = document.createElement("button");
  btn.id = "langBtn";
  btn.type = "button";
  btn.className = "lang";
  btn.setAttribute("aria-label", "Toggle language");

  btn.style.position = "fixed";
  btn.style.top = "14px";
  btn.style.right = "14px";
  btn.style.zIndex = "999";
  btn.style.padding = "7px 10px";
  btn.style.borderRadius = "999px";
  btn.style.border = "1px solid rgba(255,255,255,0.2)";
  btn.style.background = "rgba(15,17,21,0.92)";
  btn.style.color = "#ffffff";
  btn.style.backdropFilter = "blur(10px)";
  btn.style.cursor = "pointer";

  document.body.appendChild(btn);
  return btn;
}

/* =========================
   ATTACH BUTTON HANDLER
========================= */
export function attachLangButton(){
  const btn = ensureLangButton();
  if(btn){
    btn.onclick = toggleLang;
    btn.textContent = getLang().toUpperCase() + " ▾";
  }
}

/* =========================
   INIT (CLEAN)
========================= */
export function initLang(){

  // apply immediately
  applyLang();

  // attach click handler
  if(document.readyState === "loading"){
    document.addEventListener("DOMContentLoaded", attachLangButton);
  } else {
    attachLangButton();
  }
}